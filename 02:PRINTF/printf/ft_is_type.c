#include "ft_printf.h"

size_t	ft_is_type(char symb)
{
	if ((symb == 'c') || (symb == 's') || (symb == 'p') || (symb == 'd') || \
			(symb == 'i') || (symb == 'u') || (symb == 'x') || (symb == 'X') \
			|| (symb == '%'))
		return (1);
	return (0);
}
